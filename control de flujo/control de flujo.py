 #ejemplos de if con variables

if 2 != 2: #No se imprive por que es false
    print("2 es diferente a 2")
if 2 == 2:
    print("2 es igual a 2")
    
  
#para definir el control de flujo utilizamos: 
if 2 > 5:       #Si esta condición es verdadera se imprime 
  print("2 es mayor a 5") 
elif 2 > 5:     #Si esta condición if es falsa y elif es verdadera se imprime
  print("2 es menor a 5") 
else:           #Si esta condiciones anteriores no se cumplen se imprime
  print("No se cumple ninguna de las condiciones anteriores")